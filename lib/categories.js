export function getCategoryById(id) {
  return CATEGORIES.find((c) => c.id === id) || null;
}

export function pickRandomCategories(count) {
  // Mélange de Fisher-Yates : un .sort(() => Math.random() - 0.5) est biaisé
  // et favorise les premières catégories de la liste, ce qui donnait
  // l'impression que les mêmes catégories revenaient toujours.
  const shuffled = [...CATEGORIES];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled.slice(0, count).map((c) => c.id);
}
